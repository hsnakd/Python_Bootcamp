# count(element): Returns the number of occurrences of the specified element in the tuple.
my_tuple = (1, 2, 3, 1, 4, 1, 5)
count_result = my_tuple.count(1)
print(f"my_tuple.count(1) = {count_result}")  # Output: my_tuple.count(1) = 3

# index(element): Returns the index of the first occurrence of the specified element in the tuple.
index_result = my_tuple.index(4)
print(f"my_tuple.index(4) = {index_result}")  # Output: my_tuple.index(4) = 4
